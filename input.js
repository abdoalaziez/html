function calculate(operation,number1,number2){
   switch(operation){
    case "+":
        return number1+number2;
        break;  
    case "-":
        return number1-number2;
        break;
case "/":
    return number1/number2;
        break;
case "*":
    return number1*number2;
        break;
case "%":
    return number1%number2;
   }
}
console.log(calculate("+", 1 , 2))


